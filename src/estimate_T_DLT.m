function T = estimate_T_DLT(R,x,X)



M = [-1, 0, x(1,1);
    0, -1, x(2,1);
    -1, 0, x(1,2);
    0, -1, x(2,2)];

b = [R(1,:)*X(:,1) - x(1,1)*R(3,:)*X(:,1);
    R(2,:)*X(:,1) - x(2,1)*R(3,:)*X(:,1);
    R(1,:)*X(:,2) - x(1,2)*R(3,:)*X(:,2);
    R(2,:)*X(:,2) - x(2,2)*R(3,:)*X(:,2)];

[~, ~, V] = svd([M -b]);
T = V(1:3, end) / V(end, end);
end


